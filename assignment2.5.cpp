#include <iostream.h>
#include <stdlib.h>

int main()
{
      int nNum1 = 60;
      int nNum2 = 7;
      cout<<nNum1<<" + "<<nNum2<<" = "<<nNum1 + nNum2<<endl;
      cout<<nNum1<<" - "<<nNum2<<" = "<<nNum1 - nNum2<<endl;
      cout<<nNum1<<" * "<<nNum2<<" = "<<nNum1 * nNum2<<endl;
      cout<<nNum1<<" / "<<nNum2<<" = "<<nNum1 / nNum2<<" With a remainder of "<<nNum1 % nNum2<<endl;

      system("PAUSE");
      return 0;
}
